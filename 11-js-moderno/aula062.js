// Interpolação de String

// Exemplo 01
let spaceshipName = 'Colossus'
console.log(`Nome da nave: ${spaceshipName}`) // Exibe: Nome da nave: Colossus

// Exemplo 02
let newName = `Nome da nave: ${spaceshipName}`
console.log(newName) // Exibe: Nome da nave: Colossus

// Exemplo 03
let velocity = 50
console.log(`Velocidade da nave: ${velocity}km/s`) // Exibe: Velocidade da nave: 50km/s