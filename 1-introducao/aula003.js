// Recursor nativos do browser

console.log("Olá, mundo!") // Retorna mensagem neutra para o console
console.info("Isso é uma informação") // Retorna mensagem de informação para o console
console.warn("Isso é um aviso!") // Retorna mensagem de aviso para o console
console.error("Ocorreu um erro...") // Retorna mensagem de erro para o console

alert("Olá, mundo!") // Exibe um popup de alerta na tela
confirm("JavaScript é legal! Confirma essa informação?") // Exibe um popup de caixa de confirmação
prompt("Qual seu nome?") // Exibe um popup de input de dados